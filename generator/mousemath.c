#include "arkanoid.h"

void	mousemath(int *x, int *y)
{
	int mx;
	int my;

	mx = *x;
	my = *y;
	mx += 5;
	my += 5;
	mx -= mx % 10;
	my -= my % 10;
	mx -= 5;
	my -= 5;
	*x = mx;
	*y = my;
}
